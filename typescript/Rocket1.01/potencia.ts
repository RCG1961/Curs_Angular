class Propulsor{
    
    public PropulsorId:string;
    public PropulsorPower:number;
    public PropulsorActive:number;

    
    constructor(PropulsorId:string, PropulsorPower:number, PropulsorActive:number){
        this.PropulsorId = PropulsorId;
        this.PropulsorPower = PropulsorPower;
        this.PropulsorActive = PropulsorActive;

    }

}